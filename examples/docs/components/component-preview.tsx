interface ComponentPreviewProps {
  name: string;
}

export function ComponentPreview({ name }: ComponentPreviewProps) {
  const [mounted, setMounted] = React.useState(false);
  React.useEffect(() => setMounted(true), []);

  const LoadedPreview = React.useMemo(() => {
    const loader = PREVIEW_LOADERS[name];
    if (!loader) {
      return () => <div className="p-4 text-fd-muted-foreground">Example &quot;{name}&quot; not found.</div>;
    }
    return React.lazy(loader);
  }, [name]);

  if (!mounted) {
    return (
      <div
        className="not-prose example-reset w-full flex flex-col justify-center items-center min-h-[200px] rounded-lg border border-fd-border bg-fd-background p-4"
        style={{ backgroundColor: 'var(--seed-color-bg-layer-default, var(--fd-background))' }}
      >
        <div className="text-fd-muted-foreground">Loading…</div>
      </div>
    );
  }

  return (
    <React.Suspense fallback={<div className="flex min-h-[200px] items-center justify-center">Loading…</div>}>
      <div
        className="not-prose example-reset w-full flex flex-col justify-center items-center min-h-[200px] rounded-lg border border-fd-border bg-fd-background p-4"
        style={{ backgroundColor: 'var(--seed-color-bg-layer-default, var(--fd-background))' }}
      >
        <LoadedPreview />
      </div>
    </React.Suspense>
  );
}
